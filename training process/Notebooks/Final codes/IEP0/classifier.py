from pathlib import Path
from PIL import Image
import shutil

root = Path("/content/drive/MyDrive/classifier")
src_test = root / "test"
dst_test_rot = root / "test_rotated"

classes = ["book", "newspaper", "microfilm"]
exts = {".jpg", ".jpeg", ".png", ".tif", ".tiff", ".bmp", ".webp"}

# reset destination
if dst_test_rot.exists():
    shutil.rmtree(dst_test_rot)
dst_test_rot.mkdir(parents=True, exist_ok=True)

rot_ops = [
    ("rot0",   None),
    ("rot90",  Image.Transpose.ROTATE_270),  # 90 CW
    ("rot180", Image.Transpose.ROTATE_180),
    ("rot270", Image.Transpose.ROTATE_90),   # 270 CW
]

created = 0
for cls in classes:
    src_cls = src_test / cls
    dst_cls = dst_test_rot / cls
    dst_cls.mkdir(parents=True, exist_ok=True)

    for p in src_cls.iterdir():
        if p.suffix.lower() not in exts:
            continue
        with Image.open(p) as im:
            for tag, op in rot_ops:
                out = dst_cls / f"{p.stem}_{tag}{p.suffix}"
                if op is None:
                    im.save(out)  # copy original as rot0
                else:
                    im.transpose(op).save(out)
                created += 1

print("Created files in test_rotated:", created)
for cls in classes:
    print(cls, len(list((dst_test_rot / cls).glob("*"))))

