from pathlib import Path
from PIL import Image

ROOT = Path("/content/drive/MyDrive/classifier")
TRAIN = ROOT / "train"
EXTS = {".jpg", ".jpeg", ".png", ".tif", ".tiff", ".bmp", ".webp"}

print("ROOT exists:", ROOT.exists())
print("TRAIN exists:", TRAIN.exists())
if not TRAIN.exists():
    raise FileNotFoundError(f"Missing: {TRAIN}")

ROTATIONS = [
    ("rot90",  Image.Transpose.ROTATE_270),  # 90 CW
    ("rot180", Image.Transpose.ROTATE_180),
    ("rot270", Image.Transpose.ROTATE_90),   # 270 CW
]

created = 0
for class_dir in TRAIN.iterdir():
    if not class_dir.is_dir():
        continue
    for p in class_dir.iterdir():
        if p.suffix.lower() not in EXTS:
            continue
        if any(tag in p.stem for tag, _ in ROTATIONS):
            continue
        with Image.open(p) as im:
            for tag, op in ROTATIONS:
                out = class_dir / f"{p.stem}_{tag}{p.suffix}"
                if not out.exists():
                    im.transpose(op).save(out)
                    created += 1

print("Created rotated images:", created)

